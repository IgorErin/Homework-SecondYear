namespace MyNunit.TestsInfo;

/// <summary>
/// Test status enum.
/// </summary>
public enum TestStatus
{
    /// <summary>
    /// Indicate that test failed.
    /// </summary>
    Failed,

    /// <summary>
    /// Indicate that test ignored.
    /// </summary>
    Ignored,

    /// <summary>
    /// Indicate that test passed.
    /// </summary>
    Passed,
}
